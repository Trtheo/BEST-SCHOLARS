<button class="accordion">8.  How do I know if I am eligible to apply?</button>
<div class="panel">
  <p> You will know if you are eligible to apply for a particular scholarship by reading the details on eligibility found on the provider’s website and our website <a href="#">Best Scholars</a>. Eligibility may depend on nationality, if the scholarship in question is targeted at students from specific regions. For subject-specific scholarships, the program you are enrolling on will also affect your eligibility.

In most cases, university scholarships are only open to students who have already been accepted onto a program at that particular university. Government scholarships will also have designated funds for domestic students, as well as separate funds for international students – so make sure you have found the correct scholarship scheme before applying.

Other common eligibility requirements include good grades at prior level of study, a degree from a reputed university (for graduate scholarships), and proficiency in English (e.g. TOEFL/IELTS, for English-language programs). </p>
</div>

