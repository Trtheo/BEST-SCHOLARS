If you are worried about the authenticity of a scholarship, conduct an online search to see if there is any more information online about the same scholarship or vist our website in contact section , ask certain relevant question we will respond to you very quickly. If there is very little information, or the information that is provided is somewhat vague or unclear, walk away!

To minimize your chances of being taken in by a scam, stick to official websites belonging to schools, government or reputed organizations, or use a free scholarship search service. </p>
</div>

